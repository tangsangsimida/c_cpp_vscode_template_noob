#include <stdio.h>

/**
 * 知识点：C 语言程序基本结构与生命周期
 * 1. 预处理指令：#include 的作用 (引入标准库/头文件)
 * 2. 入口函数：main() 函数的结构与返回值意义
 * 3. 代码块与语句：使用 {} 组织代码，每个语句以 ; 结束
 * 4. 编译四部曲：预处理、编译、汇编、链接
 */

// --- 1. 预处理指令 ---
// #include <stdio.h> 告诉编译器引入“标准输入输出库”，这样我们才能使用 printf 函数。

int main() {
    // --- 2. 程序的起点 ---
    printf("==== 1. C 程序的基本面孔 ====\n");
    printf("[知识点] 每个 C 程序必须有一个 main 函数作为执行入口。\n");
    printf("Hello, World! 这是你的第一个 C 程序。\n");

    printf("\n==== 2. 深入理解编译流程 ====\n");
    printf("[知识点] 你写的代码到运行的 .exe 需要经历四个阶段：\n");
    printf("  1. 预处理 (Preprocessing): 处理 #include 和 #define，生成 .i 文件。\n");
    printf("  2. 编译 (Compilation): 将代码翻译成汇编语言，生成 .s 文件。\n");
    printf("  3. 汇编 (Assembly): 将汇编指令转为机器码，生成 .o (目标文件)。\n");
    printf("  4. 链接 (Linking): 将你的代码与库函数(如 printf)绑在一起，生成最终的 .exe。\n");

    printf("\n==== 3. 核心避坑指南 ====\n");
    
    // 坑点 1：分号丢失
    printf("[坑点] 每一条独立语句必须以分号 ';' 结束。忘了它会导致语法错误。\n");
    
    // 坑点 2：返回值
    printf("[坑点] 虽然 C99+ 标准允许 main 结尾略过 return，但显式写出 return 0; 是跨平台的专业做法。\n");
    printf("  - 0 代表“执行成功”，非 0 值通常代表出错了。\n");

    // 坑点 3：注释
    printf("[知识点] 使用 // 进行单行注释，使用 /* ... */ 进行多行注释。\n");

    return 0; // 程序成功结束并向操作系统报告
}

